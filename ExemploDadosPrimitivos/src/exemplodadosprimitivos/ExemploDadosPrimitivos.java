package exemplodadosprimitivos;

public class ExemploDadosPrimitivos {

    public static void main(String[] args) {
    
        String nome = "Rafael Guido";
        int idade = 19;
        double meuPeso = 79.2;
        char sexo = 'M';
        boolean legal = true;
        
        System.out.println("O nome da pessoa é: " + nome);
        System.out.println("A idade da pessoa é: " + idade);
        System.out.println("O peso da pessoa é: " + meuPeso);
        System.out.println("O gênero da pessoa é: " + sexo);
        System.out.println("Essa pessoa é legal: " + legal);
        
    }
    
}
